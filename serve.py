from flask import Flask, request, jsonify

from joblib import load


app = Flask(__name__)
svm_clf = load("svm_gamma=0.0005_C=1.joblib")

@app.route("/", methods = ["GET"])
def hello_world():
    return "<p>Hello, World!</p>"


@app.route("/Sum", methods = ["POST"])
def sum():
    print(request.json)
    m = request.json["m"]
    n = request.json["n"]
    s = m + n
    return jsonify({'s':s})


@app.route("/Pridt", methods = ['POST'])
def predict():
    img = request.json['img']

    cls1 = svm_clf.predict([img])

    # print(cls1)

    return str(cls1[0])


@app.route("/imgcm", methods = ['POST'])
def comp_img():
    img1 = request.json['img1']
    img2 = request.json['img2']

    cl1 = svm_clf.predict([img1])
    cl2 = svm_clf.predict([img2])

    fg = None
    if cl1[0] == cl2[0]:
        fg = "same"
    else:
        fg = "different"


    return str(fg)


